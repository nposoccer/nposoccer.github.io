          <p>これら課題に対して私たちピジョンは、
          <br class="spNone">『障がいのある子たちが笑顔になれる居場所であり、学び場としてあり続けること』
          <br class="spNone">ひいては、『障がい者も健常者も共に手を取り合って生きていける社会に変えていくこと』
          <br class="spNone">をミッションとして掲げています。</p>
          </div>
