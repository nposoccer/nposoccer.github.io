        <h2 id="solution">3つの取り組み</h2>
        <p>ミッションを実現するため、私たちは以下3つのことを継続的に取り組んでいきます。</p>
        <ul class="commonInner__solution">
          <li class="solution__list">
            <dl>
              <h3><dt>子どもの居場所、成長の場</dt></h3>
              <dd>①仲間と繋がり、笑顔になり、いつも安心できる居場所であり続けます。</dd>
              <dd>②チームスポーツとしてのサッカー活動を通し、明るい心・協調・自己管理を養う教育の現場となります。</dd>
            </dl>
          </li>
          <li class="solution__list">
            <dl>
              <h3><dt>保護者・学校との協力</dt></h3>
              <dd>保護者や学校関係者と共に、障がいのある人をサポートする体制を築き、可能性を広げます。</dd>
              <dd>（子どもの障がいに向き合い、可能な限り伸ばすための取り組みを行います。）</dd>
            </dl>
          </li>
          <li class="solution__list">
            <dl>
              <h3><dt>健常者との交流・情報発信</dt></h3>
              <dd>健常者との交流促進、および障がいへの理解を浸透させていきます。</dd>
              <dd>そのために、クラブ活動や地域イベントに健常者を巻き込みながら、各種オンライン媒体を通して障がいのある人の実像を発信します。</dd>
            </dl>
          </li>
        </ul>
