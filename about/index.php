				<h2 id="problem">課題感</h2>
				<p>私たち「ピジョン」は活動を始めてから、３つの課題感を持ちました。</p>
				<section class="subSummary subSummary-connection">
					<h3>１．居場所と経験の不足</h3>
					<div class="paragraph">
						<input id="paragraph-01" type="checkbox">
						<label for="paragraph-01"></label>
						<div class="paragraph-content">
							<h4>「本当は、サッカーじゃなくても良かった」</h4>
							<p>ピジョン設立から一年経ったころ、ある生徒のお母さんがそう言いました。障がいのある子どもの習いごととしてはよく、水泳や太鼓など個人でやるものが挙げられます。これに対して彼女は、お子さんが人と関わるなかで成長していくことを願い、町のクラブチームを探していました。しかし、野球にせよテニスにせよ、障がいをもった子どもを受け入れてくれるチームがなかなか見つかりません。サッカーチームも溢れるほど存在するのに、そういったチームは当時ほぼありませんでした。つまり、スポーツ種目を選ぶほどの選択肢もない＝圧倒的に経験の機会が不足しているということです。また、行政では障がいをもった子ども向けにイベントを開催することがあります。しかしそれらは年に1,2回しかありません。障がいをもった子どものなかには、時間をよくかけなければ定着しない子がいます。したがって、継続して何かを経験する機会がもっと必要なのです。</p>
							<h4>「居場所があるだけでも十分」</h4>
							<p>親御さんは、体験にお子さんを連れてきたときの話もしてくれました。「普段（その子は）大勢の人の中に入ることができず、すぐに帰ってしまいます。そしてもう『行きたくない』と言い、行かなくなってしまいます。でも、ここには『たまたま』居ることができました。体験したときは佇んでいただけですが、居れるだけでも十分だと思いました。」私たちはこれを聞いて、そうした子どもが居場所となれる環境は限られてしまうのだということが分かりました。</p>
						</div>
					</div>
				</section>
				<section class="subSummary subSummary-connection">
					<h3>２．自立と就職の道のり</h3>
					<div class="paragraph">
						<input id="paragraph-02" type="checkbox">
						<label for="paragraph-02"></label>
						<div class="paragraph-content">
							<h4>「私たちがいなくなったら」</h4>
							<p>彼女はこう続けました。「そのあと何回か活動に行くことで、少しづつボール触るようになり、人とも接するようになりました。次第に私に『サッカーいつなの？』と聞いてくるようになったり、『サッカーしにいくんだよ』って嬉しそうに先生に伝えるようにまでなりました。感謝しています。でも、もう一つ無理だと分かっていますが、お願いがあります。私たちがいる間はこの子のことをサポートできます。けど私たちがいなくなったら、この子をサポートする人がいなくなってしまう。だから、この子を自立させてほしいんです。」</p>
							<h4>「障がいをもった人の離職率は30~50%」</h4>
							<p>親にとって、子どもの自立は切実な課題だと思います。しかし残念ながら、障がいのある子どもが大人になり、せっかく就職しても、「人間関係が上手くいかない」「働く意欲・体力が続かない」「作業が合わない・適応できない」といった理由で、すぐに離職してしまう状況があります。実際に厚生労働省の調査(※)によると、就職から1年以内の離職率が全国平均で15%であるのに対して、障がいをもった人達の離職率は30~50%と高い数字になってしまっています。
							したがって、子どものころから自立を促していく必要があると考えています。</p>
							<hr commonLine-problem>
							<a href="https://www.mhlw.go.jp/file/05-Shingikai-11601000-Shokugyouanteikyoku-Soumuka/0000178930.pdf"
